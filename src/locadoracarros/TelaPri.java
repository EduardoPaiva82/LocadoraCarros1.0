    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
      
        int id = Integer.parseInt(jtIdade.getText());
      if(tipo.equals("novo")){ 
        Clientes c =  new Clientes(jtNome.getText(), jtCPF.getText(), id,jtCelular.getText());
        lst_Clientes.add(c); 
      
      } else if(tipo.equals("editar")){
          int index = tbCliente.getSelectedRow();
          lst_Clientes.get(index).setNome(jtNome.getText());
          lst_Clientes.get(index).setIdade(id);
          lst_Clientes.get(index).setCPF(jtCPF.getText());
          lst_Clientes.get(index).setCelular(jtCelular.getText());

      }
        CarregarTabela();
        tipo = "navegar";
        manipularInterface();
        
    }//GEN-LAST:event_btnSalvarActionPerformed


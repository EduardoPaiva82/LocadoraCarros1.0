    public void manipularInterface(){
        switch(tipo){
            case "navegar":
                    btnNovo.setEnabled(true);
                    btnEditar.setEnabled(false);
                    btnExcluir.setEnabled(false);
                    btnSalvar.setEnabled(false);
                    btnCancelar.setEnabled(false);
        
                    jtNome.setEnabled(false);
                    jtIdade.setEnabled(false);
                    jtCPF.setEnabled(false);
                    jtCelular.setEnabled(false);
                    
                break;
            case "novo":
                    btnNovo.setEnabled(false);
                    btnEditar.setEnabled(false);
                    btnExcluir.setEnabled(false);
                    btnSalvar.setEnabled(true);
                    btnCancelar.setEnabled(true);
        
                    jtNome.setEnabled(true);
                    jtIdade.setEnabled(true);
                    jtCPF.setEnabled(true);
                    jtCelular.setEnabled(true);
                    
                    jtNome.setText("");
                    jtIdade.setText("");
                    jtCPF.setText("");
                    jtCelular.setText("");
                break;
            case "editar":
                    btnNovo.setEnabled(false);
                    btnEditar.setEnabled(false);
                    btnExcluir.setEnabled(false);
                    btnSalvar.setEnabled(true);
                    btnCancelar.setEnabled(true);
        
                    jtNome.setEnabled(true);
                    jtIdade.setEnabled(true);
                    jtCPF.setEnabled(true);
                    jtCelular.setEnabled(true);
                break;
            case "excluir":
                    btnNovo.setEnabled(true);
                    btnEditar.setEnabled(false);
                    btnExcluir.setEnabled(false);
                    btnSalvar.setEnabled(false);
                    btnCancelar.setEnabled(false);
        
                    jtNome.setEnabled(false);
                    jtIdade.setEnabled(false);
                    jtCPF.setEnabled(false);
                    jtCelular.setEnabled(false);
                break;
            case "selecao":
                    btnNovo.setEnabled(true);
                    btnEditar.setEnabled(true);
                    btnExcluir.setEnabled(true);
                    btnSalvar.setEnabled(false);
                    btnCancelar.setEnabled(false);
        
                    jtNome.setEnabled(false);
                    jtIdade.setEnabled(false);
                    jtCPF.setEnabled(false);
                    jtCelular.setEnabled(false);
                break;
                
            default:
                System.out.println("Tipo Inválido!\n");
        }
    }
